// --- Context Definition ---
interface NotificationContextType {
  notifications: NotificationItem[];
  unreadCount: number;
  markAsRead: (id: number) => void;
  markAllAsRead: () => void;
}

const NotificationContext = createContext<NotificationContextType | undefined>(undefined);

export function NotificationProvider({ children }: { children: ReactNode }) {
  // ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô‡πÄ‡∏õ‡πá‡∏ô Array ‡∏ß‡πà‡∏≤‡∏á‡∏Å‡πà‡∏≠‡∏ô ‡πÅ‡∏•‡πâ‡∏ß‡∏Ñ‡πà‡∏≠‡∏¢ Set ‡∏ï‡∏≤‡∏° Role
  const [notifications, setNotifications] = useState<NotificationItem[]>([]);
  const { user } = useAuth(); // ‚úÖ ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• User ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏î‡∏π Role

  // ‚úÖ Effect: ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∏‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• Notification ‡∏ï‡∏≤‡∏° Role ‡∏Ç‡∏≠‡∏á User
  useEffect(() => {
    if (!user) {
      setNotifications([]);
      return;
    }

    switch (user.role) {
      case 'student':
        setNotifications(studentNotifications);
        break;
      case 'teacher':
        setNotifications(teacherNotifications);
        break;
      case 'parent':
        setNotifications(parentNotifications);
        break;
      default:
        setNotifications([]);
    }
  }, [user]); // ‡∏£‡∏±‡∏ô‡πÉ‡∏´‡∏°‡πà‡πÄ‡∏°‡∏∑‡πà‡∏≠ User (Role) ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô

  const markAsRead = (id: number) => {
    setNotifications(prev => prev.map(n => 
      n.id === id ? { ...n, isRead: true } : n
    ));
  };

  const markAllAsRead = () => {
    setNotifications(prev => prev.map(n => ({ ...n, isRead: true })));
  };

  const unreadCount = notifications.filter(n => !n.isRead).length;

  return (
    <NotificationContext.Provider value={{ notifications, unreadCount, markAsRead, markAllAsRead }}>
      {children}
    </NotificationContext.Provider>
  );
}

